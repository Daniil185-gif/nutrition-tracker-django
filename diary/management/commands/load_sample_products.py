class Command(BaseCommand):
    help = 'Loads a starter database of popular products and ready meals.'

    def handle(self, *args, **options):
        created_count = 0
        updated_count = 0

        for name, calories, proteins, fats, carbs in PRODUCTS:
            product, created = Product.objects.update_or_create(
                name=name,
                defaults={
                    'calories_per_100g': calories,
                    'proteins': Decimal(proteins),
                    'fats': Decimal(fats),
                    'carbs': Decimal(carbs),
                },
            )
            if created:
                created_count += 1
            else:
                updated_count += 1

        self.stdout.write(
            self.style.SUCCESS(
                f'Loaded products: {created_count} created, {updated_count} updated.',
            ),
        )
